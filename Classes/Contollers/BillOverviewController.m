#pragma mark - View lifecycle
- (void)viewDidLoad
{
    [super viewDidLoad];
    NSBundle* bundle = [NSBundle mainBundle];
    NSString* plistPath = [bundle pathForResource:@"Bill" ofType:@"plist"];
    NSDictionary *billData = [[NSDictionary alloc] initWithContentsOfFile:plistPath];
    self.tableData = [billData objectForKey:@"Values"];
    [billData release];
    self.tableView.backgroundColor = [UIColor clearColor];
    UIImageView *imageView = [[UIImageView alloc] initWithImage:[UIImage imageNamed:@"Background.png"]];
    self.tableView.backgroundView = imageView;
    [imageView release];
    
    keyArray = [[NSArray alloc] initWithObjects:@"Izdavatelj", @"Opis usluge", @"Broj računa", @"Za period", @"Iznos računa", @"Datum dospijeća", nil];
